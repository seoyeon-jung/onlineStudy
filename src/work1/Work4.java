package work1;

public class Work4 {

	public static void main(String[] args) {
		// 문자형 (char)
		char a = 'S';
		System.out.println(a);
		
		// 문자열 (String)
		String study = "코뮤니티";
		System.out.println(study);
		
		// 실수형 (float) : f 붙어야 float 자료형으로 인식
		float num_f=0.314f;
		System.out.println(num_f);
		
		// 실수형 (double) : float 보다 범위 더 큼
		double num_d=0.123456789;
		System.out.println(num_d);
		
		// 논리형 (boolean)
		boolean game1 = true;
		boolean game2 = false;
		System.out.println(game1);
		System.out.println(game2);

	}

}
